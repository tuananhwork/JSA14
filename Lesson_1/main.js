// Vòng lặp: for, while, do-while

// for
for (let i = 0; i < 5; i++) {
  console.log(i);
}

// while
let a = 0;
while (a < 5) {
  console.log(a);
  a++;
}

// Do - while
let i = 10;

do {
  console.log(i);
  i++;
} while (i < 10);
